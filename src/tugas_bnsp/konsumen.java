    private void konsumenBaruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_konsumenBaruActionPerformed
        try {
            
            pst = con.prepareStatement("insert into konsumen(nama_depan,nama_belakang, notelp) values(?,?,?)");
            
            pst.setString(1, namaDepan.getText());
            pst.setString(2, namaBelakang.getText());
            pst.setString(3, noTelp.getText());
            pst.executeUpdate();
            
            pst = con.prepareStatement("select MAX(id) from konsumen ");
            rs = pst.executeQuery();
            if(rs.next()){
                this.idKonsumen = rs.getString("MAX(id)");
            }
            
            
            
            JOptionPane.showMessageDialog(this, "Konsumen telah terdaftar");
            
            id.setText("masukkan apabila pengguna lama");
            namaDepan.setText("");
            namaBelakang.setText("");
            noTelp.setText("");
            id.requestFocus();
            
            pemesanan pesan = new pemesanan();
            pesan.setIdk(idKonsumen);
            pesan.setVisible(true);
            setVisible(false); 
            
        }catch (SQLException ex) {
            
           JOptionPane.showMessageDialog(null,ex);
          
        }
    }//GEN-LAST:event_konsumenBaruActionPerformed

