    private void konsumenLamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_konsumenLamaActionPerformed
        try {
            
            pst = con.prepareStatement("select * from konsumen where id =? and nama_depan =? and nama_belakang =?");
            pst.setString(1, id.getText());
            pst.setString(2, namaDepan.getText());
            pst.setString(3, namaBelakang.getText());
            
            rs= pst.executeQuery();
            
            
            if(rs.next())
            {
               this.idKonsumen = id.getText();
               pemesanan pesan = new pemesanan();
               pesan.setIdk(idKonsumen);
               pesan.setVisible(true);
               setVisible(false); 
               System.out.println(idKonsumen);
            }
            
            else
            {
                JOptionPane.showMessageDialog(null,"Pengguna Tidak Ditemukan");
                id.setText("masukkan apabila pengguna lama");
                namaDepan.setText("");
                namaBelakang.setText("");
                noTelp.setText("");
                System.out.println(idKonsumen);
            }
            con.close();
        }catch (SQLException ex) {
            
           JOptionPane.showMessageDialog(null,ex);
          
        }
        
    }//GEN-LAST:event_konsumenLamaActionPerformed

