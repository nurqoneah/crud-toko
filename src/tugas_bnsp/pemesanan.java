    private void menuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuActionPerformed
        try{
            kuantitas.setText("1");
            pst = con.prepareStatement("select * from menu where nama=? and kategori=?");
            pst.setString(1, (String)menu.getSelectedItem());
            pst.setString(2, (String)kategori.getSelectedItem());
            
            rs= pst.executeQuery();
            if(rs.next()){
                String hargaMenu =rs.getString("harga");
                harga.setText(hargaMenu);
                String sisaMenu =rs.getString("kuantitas");
                sisa.setText(sisaMenu);
                String idMenu = rs.getString("id");
                setId(idMenu); 
                runTotal();
            }
        }catch (Exception ex) {
            
           JOptionPane.showMessageDialog(null,ex);
          
        }
    }//GEN-LAST:event_menuActionPerformed

