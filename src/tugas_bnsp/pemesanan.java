    private void kategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kategoriActionPerformed
        if(kategori.getSelectedItem().equals("Makanan")){
            menu.removeAllItems();
            menu.addItem("Mie Ayam");
            menu.addItem("Bakso");
            menu.addItem("Soto");
            menu.setSelectedItem(null);
        }else if(kategori.getSelectedItem().equals("Minuman")){
            menu.removeAllItems();
            menu.addItem("Coca-cola");
            menu.addItem("Teh");
            menu.addItem("Pop Ice");
            menu.setSelectedItem(null);
        }else if(kategori.getSelectedItem().equals("Lain-lain")){
            menu.removeAllItems();
            menu.addItem("Kerupuk");
            menu.addItem("Tahu");
            menu.addItem("Kerupuk Kulit");
            menu.setSelectedItem(null);
        }
    }//GEN-LAST:event_kategoriActionPerformed

